// Vertical breathing room (em units) between the base glyph and a mark.
const MARK_GAP = 30;
// Horn sits at the upper-right of the base, with its bottom near the base's middle.
const HORN_RIGHT_INSET = 20;
const HORN_TOP_DROP = 80;

// Mark position relative to the base it's stacked onto. The composer reads
// these to figure out where to put each accent — including stacked marks like
// ổ (o + circumflex + hook).
export type MarkPosition = 'above' | 'below' | 'upper-right';

const MARK_POSITIONS: Readonly<Record<number, MarkPosition>> = {
  0x00b4: 'above', // ´ acute
  0x0060: 'above', // ` grave
  0x02c6: 'above', // ˆ circumflex
  0x00a8: 'above', // ¨ diaeresis
  0x02dc: 'above', // ˜ small tilde
  0x02d8: 'above', // ˘ breve
  0xe000: 'above', //    hook above
  0xe001: 'below', //    dot below
  0xe002: 'upper-right', // horn
};

