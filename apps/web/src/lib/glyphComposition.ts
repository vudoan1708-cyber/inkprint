export const COMPOSITION_RECIPES = RECIPES;

// Transitive reverse map: composed cps that depend on each cp (base, mark, or source).
export const DEPENDENTS_OF: ReadonlyMap<number, ReadonlySet<number>> = (() => {
  const direct = new Map<number, Set<number>>();
  for (const [composedStr, recipe] of Object.entries(RECIPES)) {
    const composed = Number(composedStr);
    const deps = 'from' in recipe ? [recipe.from] : [recipe.base, ...recipe.marks];
    for (const dep of deps) {
      let s = direct.get(dep);
      if (!s) {
        s = new Set();
        direct.set(dep, s);
      }
      s.add(composed);
    }
  }
  const out = new Map<number, Set<number>>();
  for (const cp of direct.keys()) {
    const seen = new Set<number>();
    const queue: number[] = [...(direct.get(cp) ?? [])];
    while (queue.length > 0) {
      const c = queue.shift()!;
      if (seen.has(c)) continue;
      seen.add(c);
      const downstream = direct.get(c);
      if (downstream) for (const d of downstream) queue.push(d);
    }
    out.set(cp, seen);
  }
  return out;
})();

// Stable, dependency-aware ordering: every recipe appears after the recipe
// for its base (if any). composeAll relies on this so chained bases like
// ô → ổ resolve in one pass.
export const COMPOSABLE_TARGETS: readonly number[] = (() => {
  const all = Object.keys(RECIPES).map((k) => Number(k));
  const recipeMap = RECIPES;
  const visited = new Set<number>();
  const out: number[] = [];
  const visit = (cp: number): void => {
    if (visited.has(cp)) return;
    const r = recipeMap[cp];
    if (r && 'base' in r && recipeMap[r.base]) visit(r.base);
    visited.add(cp);
    if (r) out.push(cp);
  };
  for (const cp of all) visit(cp);
  return out;
})();

export type StrokeLookup = (codePoint: number) => Stroke[] | null | undefined;

export type ComposeResult =
  | { ok: true; strokes: Stroke[] }
  | { ok: false; missing: number[] };

// Returns composed strokes for `target`, or the list of code points still
// missing from `lookup`. `lookup` is expected to return both user-drawn and
// previously-composed strokes — composeAll passes a working map for that.
export function composeGlyph(target: number, lookup: StrokeLookup): ComposeResult {
  const recipe = RECIPES[target];
  if (!recipe) return { ok: false, missing: [] };

  if ('from' in recipe) {
    const source = lookup(recipe.from);
    if (!source || source.length === 0) return { ok: false, missing: [recipe.from] };
    return { ok: true, strokes: recipe.build(source) };
  }

  const missing: number[] = [];
  const baseStrokes = lookup(recipe.base);
  if (!baseStrokes || baseStrokes.length === 0) missing.push(recipe.base);
  for (const mark of recipe.marks) {
    const ms = lookup(mark);
    if (!ms || ms.length === 0) missing.push(mark);
  }
  if (missing.length > 0) return { ok: false, missing };

  const hasAboveMark = recipe.marks.some((m) => MARK_POSITIONS[m] === 'above');
  const isDottedBase = recipe.base === 0x69 || recipe.base === 0x6a;
  const initialBase =
    isDottedBase && hasAboveMark ? stripTittle(baseStrokes!) : cloneStrokes(baseStrokes!);

  let accumulated: Stroke[] = initialBase;
  for (const markCp of recipe.marks) {
    const markStrokes = lookup(markCp)!;
    const position = resolveMarkPosition(markCp, recipe.base);
    const placed = placeMark(markStrokes, accumulated, position);
    accumulated = [...accumulated, ...placed];
  }
  return { ok: true, strokes: accumulated };
}

export type ComposeAllOptions = {
  // When true, source='composed' glyphs are overwritten on every pass so a
  // tweak to a primitive flows through to every dependent. drawnSource entries
  // are always left alone.
  readonly drawnCodePoints: ReadonlySet<number>;
};

// Walks COMPOSABLE_TARGETS in dependency order, composing every target whose
// dependencies resolve. Returns just the newly-composed glyphs.
export function composeAll(
  initial: ReadonlyMap<number, Stroke[]>,
  options: ComposeAllOptions,
): Map<number, Stroke[]> {
  const working = new Map<number, Stroke[]>(initial);
  const produced = new Map<number, Stroke[]>();
  const lookup: StrokeLookup = (cp) => working.get(cp) ?? null;

  for (const target of COMPOSABLE_TARGETS) {
    if (options.drawnCodePoints.has(target)) continue;
    const result = composeGlyph(target, lookup);
    if (!result.ok) continue;
    working.set(target, result.strokes);
    produced.set(target, result.strokes);
  }
  return produced;
}

type BBox = { minX: number; minY: number; maxX: number; maxY: number };

export function strokesBoundingBox(strokes: readonly Stroke[]): BBox | null {
  let minX = Infinity;
  let minY = Infinity;
  let maxX = -Infinity;
  let maxY = -Infinity;
  let any = false;
  for (const s of strokes) {
    for (const p of s) {
      any = true;
      if (p.x < minX) minX = p.x;
      if (p.x > maxX) maxX = p.x;
      if (p.y < minY) minY = p.y;
      if (p.y > maxY) maxY = p.y;
    }
  }
  return any ? { minX, minY, maxX, maxY } : null;
}

function cloneStrokes(strokes: readonly Stroke[]): Stroke[] {
  return strokes.map((s) => s.map((p) => ({ ...p })));
}

function translateStrokes(strokes: readonly Stroke[], dx: number, dy: number): Stroke[] {
  return strokes.map((s) =>
    s.map((p) => ({
      x: p.x + dx,
      y: p.y + dy,
      pressure: p.pressure,
      ...(p.isAnchor ? { isAnchor: true as const } : {}),
    })),
  );
}

// Scales horizontally about the centre to a target width (turns the hyphen into dashes).
function scaleXAround(strokes: readonly Stroke[], targetWidth: number): Stroke[] {
  const box = strokesBoundingBox(strokes);
  if (!box || box.maxX === box.minX) return cloneStrokes(strokes);
  const cx = (box.minX + box.maxX) / 2;
  const factor = targetWidth / (box.maxX - box.minX);
  return strokes.map((s) =>
    s.map((p) => ({
      x: cx + (p.x - cx) * factor,
      y: p.y,
      pressure: p.pressure,
      ...(p.isAnchor ? { isAnchor: true as const } : {}),
    })),
  );
}

function moveTopTo(strokes: readonly Stroke[], topY: number): Stroke[] {
  const box = strokesBoundingBox(strokes);
  if (!box) return cloneStrokes(strokes);
  return translateStrokes(strokes, 0, topY - box.minY);
}

// Lays `count` copies left-to-right, each gap × the mark width apart.
function repeatRight(strokes: readonly Stroke[], count: number, gapRatio: number): Stroke[] {
  const box = strokesBoundingBox(strokes);
  if (!box) return cloneStrokes(strokes);
  const step = (box.maxX - box.minX) * (1 + gapRatio);
  const out: Stroke[] = [];
  for (let i = 0; i < count; i++) out.push(...translateStrokes(strokes, i * step, 0));
  return out;
}

// A comma lifted to quote height.
function singleQuote(comma: readonly Stroke[]): Stroke[] {
  return moveTopTo(comma, QUOTE_TOP_Y);
}

function doubleQuote(comma: readonly Stroke[]): Stroke[] {
  return repeatRight(singleQuote(comma), 2, QUOTE_PAIR_GAP_RATIO);
}

// The crossbar's position depends on the base (d vs D); every other mark is
// keyed by the mark alone.
function resolveMarkPosition(markCp: number, baseCp: number): MarkPosition {
  if (markCp === BAR_MARK) {
    return baseCp === 0x44 ? 'strike-mid-left' : 'strike-upper-right';
  }
  return MARK_POSITIONS[markCp] ?? 'above';
}

function placeMark(
  mark: readonly Stroke[],
  accumulated: readonly Stroke[],
  position: MarkPosition,
): Stroke[] {
  const aBox = strokesBoundingBox(mark);
  const bBox = strokesBoundingBox(accumulated);
  if (!aBox || !bBox) return cloneStrokes(mark);

  if (position === 'above') {
    const aCenterX = (aBox.minX + aBox.maxX) / 2;
    const bCenterX = (bBox.minX + bBox.maxX) / 2;
    return translateStrokes(mark, bCenterX - aCenterX, bBox.minY - MARK_GAP - aBox.maxY);
  }

  if (position === 'below') {
    const aCenterX = (aBox.minX + aBox.maxX) / 2;
    const bCenterX = (bBox.minX + bBox.maxX) / 2;
    return translateStrokes(mark, bCenterX - aCenterX, bBox.maxY + MARK_GAP - aBox.minY);
  }

  if (position === 'strike-upper-right' || position === 'strike-mid-left') {
    const stemX = position === 'strike-mid-left' ? STRIKE_LEFT_STEM_X : STRIKE_RIGHT_STEM_X;
    const yFrac = position === 'strike-mid-left' ? 0.5 : STRIKE_UPPER_Y;
    const targetX = bBox.minX + (bBox.maxX - bBox.minX) * stemX;
    const targetY = bBox.minY + (bBox.maxY - bBox.minY) * yFrac;
    const aCenterX = (aBox.minX + aBox.maxX) / 2;
    const aCenterY = (aBox.minY + aBox.maxY) / 2;
    return translateStrokes(mark, targetX - aCenterX, targetY - aCenterY);
  }

  // 'upper-right' — horn anchors to the base's upper-right corner.
  const targetRight = bBox.maxX + HORN_RIGHT_INSET;
  const targetTop = bBox.minY + HORN_TOP_DROP;
  return translateStrokes(mark, targetRight - aBox.minX, targetTop - aBox.minY);
}

// i/j tittle removal: the dot is the shortest stroke AND sits fully above the
// rest (its lowest point is still above the rest's highest point). The
// "fully above" check stops us nuking a t-bar or an f-bar by accident.
function stripTittle(strokes: readonly Stroke[]): Stroke[] {
  if (strokes.length < 2) return cloneStrokes(strokes);
  let smallestIdx = 0;
  let smallestLen = strokeLength(strokes[0]!);
  for (let i = 1; i < strokes.length; i++) {
    const len = strokeLength(strokes[i]!);
    if (len < smallestLen) {
      smallestIdx = i;
      smallestLen = len;
    }
  }
  const smallest = strokes[smallestIdx]!;
  const rest = strokes.filter((_, i) => i !== smallestIdx);
  const smallestBox = strokesBoundingBox([smallest]);
  const restBox = strokesBoundingBox(rest);
  if (!smallestBox || !restBox) return cloneStrokes(strokes);
  if (smallestBox.maxY >= restBox.minY) return cloneStrokes(strokes);
  return rest.map((s) => s.map((p) => ({ ...p })));
}

function strokeLength(stroke: Stroke): number {
  if (stroke.length < 2) return 0;
  let d = 0;
  for (let i = 1; i < stroke.length; i++) {
    const a = stroke[i - 1]!;
    const b = stroke[i]!;
    d += Math.hypot(b.x - a.x, b.y - a.y);
  }
  return d;
}
