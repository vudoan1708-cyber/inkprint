const MARK_GAP = 30;
// Horn sits at the upper-right of the base, with its bottom near the base's middle.
const HORN_RIGHT_INSET = 20;
const HORN_TOP_DROP = 80;

const BAR_MARK = 0x2d;
// đ/Đ crossbar placement, as fractions of the base bounding box.
